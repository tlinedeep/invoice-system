_secret_file = os.path.join(BASE_DIR, ".secret_key")

def _get_secret_key():
    env_key = os.environ.get("SECRET_KEY")
    if env_key:
        return env_key
    if os.path.exists(_secret_file):
        with open(_secret_file, "r") as f:
            return f.read().strip()
    key = secrets.token_hex(32)
    with open(_secret_file, "w") as f:
        f.write(key)
    return key

